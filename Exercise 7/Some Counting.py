#Loop 1: Counts up from 0 to 50 in increments of 1
print("Loop 1: Counting up from 0 to 50:")
for i in range(0, 51, 1):  # Start at 0, end at 50, step by 1
    print(i)
#Loop 2: Counts down from 50 to 0 in decrements of 1
print("\nLoop 2: Counting down from 50 to 0:")
for i in range(50, -1, -1):  # Start at 50, end at 0, step by -1
    print(i)
#Loop 3: Counts up from 30 to 50 in increments of 1
print("\nLoop 3: Counting up from 30 to 50:")
for i in range(30, 51, 1):  # Start at 30, end at 50, step by 1
    print(i)
#Loop 4: Counts down from 50 to 10 in decrements of 2
print("\nLoop 4: Counting down from 50 to 10 in steps of 2:")
for i in range(50, 9, -2):  # Start at 50, end at 10, step by -2
    print(i)
#Loop 5: Counts up from 100 to 200 in increments of 5
print("\nLoop 5: Counting up from 100 to 200 in steps of 5:")
for i in range(100, 201, 5):  # Start at 100, end at 200, step by 5
    print(i)